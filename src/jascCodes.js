// Returns human-readable description for a JASC code.
// Tries exact 4-digit match first, then 2-digit chapter fallback.
export function jascLabel(code) {
  if (!code) return null;
  const c = String(code).trim();
  if (JASC_CODES[c]) return JASC_CODES[c];
  if (c.length >= 4) {
    const chapter = c.slice(0, 2);
    if (JASC_CODES[chapter]) return JASC_CODES[chapter] + " (general)";
  }
  return null;
}
